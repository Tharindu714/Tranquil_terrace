    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String eqname = jTextField1.getText();
        String catagory = jComboBox1.getSelectedItem().toString();
        String department = jComboBox2.getSelectedItem().toString();
        String status = jComboBox3.getSelectedItem().toString();

        if (eqname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter the equipment name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (catagory.equals(0)) {
            JOptionPane.showMessageDialog(this, "Please Select the Catagory", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet resultSet = MySQL.execute("SELECT * FROM `hotel_eq`"
                    + "WHERE `name`='" + eqname+ "'");

                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "This Equipment is Already Added", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {
                    MySQL.execute("INSERT INTO "
                        + "`hotel_eq`(`name`,`eq_catagory_id`,`department_id`,`eq_status_id`)"
                        + "VALUES('" + eqname+ "','" + typeMap.get(catagory) + "','" + typeMap.get(department) + "','" + typeMap.get(status) + "')");
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

            JOptionPane.showMessageDialog(this, "Equipment Added Successfully", "SUCCESSFUL", JOptionPane.INFORMATION_MESSAGE);
            reset();
            
            

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try {
            int selectedRow = jTable1.getSelectedRow();
            if (selectedRow != -1) {
                String id = jTable1.getValueAt(selectedRow, 0).toString();
                String name = jTextField1.getText();
                String status = jComboBox1.getSelectedItem().toString();
                String department = jComboBox2.getSelectedItem().toString();
                String catagory = jComboBox3.getSelectedItem().toString();

                if (name.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Please Update Equipment Name", "Warning", JOptionPane.WARNING_MESSAGE);
                } else if (status.equals("Active")) {
                    JOptionPane.showMessageDialog(this, "You Update this equipment as an active equipment", "Warning", JOptionPane.WARNING_MESSAGE);

                    MySQL.execute("UPDATE `hotel_eq` SET "
                        + "`name` = '" + name+ "', "
                        + "`eq_catagory_id` = '" +catagory+ "', "
                        + "`department_id` = '" + department+ "', "
                        + "`eq_status_id` = '1' WHERE `id` = '" + id + "'");
                    reset();
                    JOptionPane.showMessageDialog(this, "Equipment Updated Successfully", "SUCCESSFUL", JOptionPane.INFORMATION_MESSAGE);
                    loadEquipment("SELECT * FROM `hotel_eq`");
                    loadCatagory();
                    loadDepartment();

                } else if (status.equals("Inactive")) {
                    JOptionPane.showMessageDialog(this, "You Update this equipment as an Inactive equipment", "Warning", JOptionPane.WARNING_MESSAGE);

                    MySQL.execute("UPDATE `employee` SET "
                        + "`name` = '" +name + "', "
                        + "`eq_catagory_id` = '" +catagory+ "', "
                        + "`department_id` = '" + department+ "', "
                        + "`eq_status_id` = '0' WHERE `id` = '" + id + "'");
                    reset();
                    JOptionPane.showMessageDialog(this, "Equipment Updated Successfully", "SUCCESSFUL", JOptionPane.INFORMATION_MESSAGE);
                    loadEquipment("SELECT * FROM `hotel_eq`");
                    loadCatagory();
                    loadDepartment();
                } else {
                    JOptionPane.showMessageDialog(this, "You Updated Equipment Status", "Warning", JOptionPane.WARNING_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Please select Specific Equipment", "Message", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

