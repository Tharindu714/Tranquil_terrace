    private void loadEquipment(String query) {
        try {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            ResultSet resultSet = MySQL.execute(query);

            while (resultSet.next()) {
                Vector v = new Vector();
                v.add(resultSet.getString("id"));
                v.add(resultSet.getString("name"));
                v.add(resultSet.getString("eq_catagory_id"));
                v.add(resultSet.getString("department_id"));
                String status = resultSet.getString("eq_status_id");
                if (status.equals("1")) {
                    v.add("Active");
                } else {
                    v.add("Inactive");
                }
                model.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }
    
     private void loadCatagory() {
        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `eq_catagory`");

            Vector v = new Vector();
            v.add("Select");

            while (resultSet.next()) {
                v.add(resultSet.getString("eq_catagory_id"));
                typeMap.put(resultSet.getString("eq_catagory_id"), resultSet.getString("id"));
            }

            DefaultComboBoxModel comboBoxModel = new DefaultComboBoxModel(v);
            jComboBox3.setModel(comboBoxModel);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
     
