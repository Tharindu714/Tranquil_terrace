    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        try {
            DefaultTableModel tgbc = (DefaultTableModel) jTable1.getModel();
            tgbc.setRowCount(0);

            ResultSet rest = MySQL.execute("SELECT DISTINCT * FROM `rooms` INNER JOIN `room_type` ON `rooms`.`room_type_id`=`room_type`.`id` \n"
                    + "INNER JOIN `room_status` ON `rooms`.`room_status_id`=`room_status`.`id`");

            while (rest.next()) {

                int id = rest.getInt("id");
                String name = rest.getString("room_name");
                String status = rest.getString("status");
                String type = rest.getString("type");
                int price = rest.getInt("price");

                Vector vtc = new Vector();
                vtc.add(id);
                vtc.add(name);
                vtc.add(status);

                vtc.add(type);
                vtc.add(price);

                tgbc.addRow(vtc);

            }

        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton4ActionPerformed

