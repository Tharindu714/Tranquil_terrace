        // TODO add your handling code here:

        String roomname = jTextField1.getText();
        String price = jTextField2.getText();
        String status = jComboBox1.getSelectedItem().toString();
        String roomtype = jComboBox2.getSelectedItem().toString();

        if (roomname == null) {
            JOptionPane.showMessageDialog(this, "Please enter room name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (status.equals(0)) {
            JOptionPane.showMessageDialog(this, "Please select room status", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (roomtype.equals(0)) {
            JOptionPane.showMessageDialog(this, "Please select room type", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (price == null) {
            JOptionPane.showMessageDialog(this, "Please Enter room price", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                MySQL.execute("INSERT INTO `rooms`  (`room_name`,`price`,`room_type_id`,`room_status_id`) VALUE ('" + roomname
                        + "','" + price + "','1','3')");

                JOptionPane.showMessageDialog(this, "new Added Successfully", "SUCCESSFUL", JOptionPane.INFORMATION_MESSAGE);

                reset();

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
