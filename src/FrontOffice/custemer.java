        // TODO add your handling code here:
        String fname = jTextField1.getText();
        String nop = jTextField4.getText();
        String mobile = jTextField5.getText();
        ButtonModel gender = buttonGroup1.getSelection();
        String addl1 = jTextField8.getText();
        String addl2 = jTextField9.getText();
        String city = String.valueOf(jComboBox3.getSelectedItem());
        String country = String.valueOf(jComboBox4.getSelectedItem());

        if (fname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Your Full Name", "Warning", JOptionPane.ERROR_MESSAGE);

        } else if (nop.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Your NIC or Passport", "Warning", JOptionPane.ERROR_MESSAGE);

        } else if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Your Mobile Number", "Warning", JOptionPane.ERROR_MESSAGE);

        } else if (gender == null) {
            JOptionPane.showMessageDialog(this, "Please Select Your Gender", "Warning", JOptionPane.ERROR_MESSAGE);

        } else if (addl1.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Your Address Line 1", "Warning", JOptionPane.ERROR_MESSAGE);

        } else if (addl2.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Your Address Line 2", "Warning", JOptionPane.ERROR_MESSAGE);

        } else if (city.equals("Select City")) {
            JOptionPane.showMessageDialog(this, "Please Select Your  City", "Warning", JOptionPane.ERROR_MESSAGE);

        } else if (country.equals("Select Country")) {
            JOptionPane.showMessageDialog(this, "Please Select Your country", "Warning", JOptionPane.ERROR_MESSAGE);

        } else {
            String genderId = gender.getActionCommand();
            int cityId = CityMap.get(city);
            int countryid = CountryMap.get(country);

            try {
                ResultSet resultSet = MySQL.execute("SELECT * FROM `customer`"
                        + "WHERE `full_name`='" + fname + "' AND `nic/passport`='" + nop + "'");

                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "This customer already exists", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {
                    MySQL.execute("INSERT INTO `customer` (`nic/passport`, `mobile`, `full_name`,`gender_id`)\n"
                            + "VALUES ('"+nop+"', '"+mobile+"', '"+fname+"', '"+genderId+"');\n"
                            + "\n"
                            + "INSERT INTO `customer_address` (`line1`, `line2`, `customer_city_id`, `customer_nic/passport`)\n"
                            + "VALUES ('"+addl1+"', '"+addl2+"','"+cityId+"', '"+nop+"');"
                    );

                    JOptionPane.showMessageDialog(this, "successfully registered",
                            "information", JOptionPane.INFORMATION_MESSAGE);

                    loadCustomer();
                    reset();
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
