    public void loadCustomer() {

        try {
            ResultSet resultSet = MySQL.execute(
                    "SELECT `full_name`,`nic/passport`,`mobile`,`gender`,`line1`,`line2`,`name` FROM `customer` "
                    + "INNER JOIN `gender` ON `customer`.`gender_id` = `gender`.`id`"
                    + "INNER JOIN `customer_address` ON `customer_address`.`customer_nic/passport`= `customer`.`nic/passport`"
                    + "INNER JOIN `customer_city` ON `customer_city`.`id` = `customer_address`.`customer_city_id`");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();

                vector.add(resultSet.getString("full_name"));
                vector.add(resultSet.getString("nic/passport"));
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("gender.gender"));
                
                vector.add(resultSet.getString("customer_address.line1")+" "+
                resultSet.getString("customer_address.line2")+" "+
                resultSet.getString("customer_city.name"));

                model.addRow(vector);
                jTable1.setModel(model);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void loadCities() {

        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `city`");

            Vector vector = new Vector();
            vector.add("Select City");

            while (resultSet.next()) {
                vector.add(resultSet.getString("name"));
                CityMap.put(resultSet.getString("name"), resultSet.getInt("id"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            jComboBox3.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

